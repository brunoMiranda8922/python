dic = {"Nome": "Bruno", "Idade": "21 anos"}
print(dic, '\n') #Exibe todos os elementos do dicionário. 

print(dic["Nome"], dic["Idade"], '\n') #Retornando o valor associado à uma chave, notação de indices[]

dic["Nome"] = "Jose" #Atribuindo um novo valor na chave "Nome" 
print(dic, '\n') 


 

